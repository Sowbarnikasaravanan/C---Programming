// To divide a by b
#include<iostream>
using namespace std;
int main(){
    int a,b,div;
    cout<<"Enter the first number:";
    cin>>a;
    cout<<"Enter the second number:";
    cin>>b;
    div=a/b;
    cout<<"The quotient when "<<a<<" divided by "<<b<<" is "<<div<<endl;
    return 0;
}

/*
Sample output:
Enter the first number:36
Enter the second number:12
The quotient when 36 divided by 12 is 3
*/
